const supportedLocales = {
    ru: "ru-RU",
    en: "en-US",
    fr: "fr-FR",
    he: "he-IL",
};

// i18n Manager
const i18n = {
    _lang: translations[localStorage.getItem('app_lang')] ? localStorage.getItem('app_lang') : 'en',

    get lang() {
        return this._lang;
    },

    set lang(newLang) {
        this._lang = translations[newLang] ? newLang : 'en';
        localStorage.setItem('app_lang', this._lang);
        document.documentElement.lang = this._lang;
        document.documentElement.dir = this._lang === 'he' ? 'rtl' : 'ltr';
        this.apply();
    },

    t(key) {
        return translations[this._lang]?.[key] || translations.en?.[key] || translations.ru?.[key] || key;
    },

    locale() {
        return supportedLocales[this._lang] || supportedLocales.en;
    },

    apply() {
        // Update all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            el.textContent = this.t(key);
        });

        // Update all elements with data-i18n-placeholder
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = this.t(key);
        });

        // Update all elements with data-i18n-html
        document.querySelectorAll('[data-i18n-html]').forEach(el => {
            const key = el.getAttribute('data-i18n-html');
            el.innerHTML = this.t(key);
        });

        // Update title
        document.title = this.t('app_title');
    },

    init() {
        document.documentElement.lang = this._lang;
        document.documentElement.dir = this._lang === 'he' ? 'rtl' : 'ltr';
        this.apply();
    }
};
